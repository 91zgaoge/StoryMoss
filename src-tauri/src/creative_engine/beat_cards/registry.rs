//! 每张卡都是公共领域常识级的叙事归纳，不引用任何具体作品台词、人名或情节链。

use super::{BeatCard, BeatCardCategory};

fn c(
    id: &str,
    name: &str,
    category: BeatCardCategory,
    function: &str,
    when_to_use: &str,
    remix_hint: &str,
    avoid: &str,
    tags: &[&str],
) -> BeatCard {
    BeatCard {
        id: format!("beat_card.{}", id),
        name: name.to_string(),
        category,
        function: function.to_string(),
        when_to_use: when_to_use.to_string(),
        remix_hint: remix_hint.to_string(),
        avoid: avoid.to_string(),
        tags: tags.iter().map(|s| s.to_string()).collect(),
    }
}

pub fn builtin_beat_cards() -> Vec<BeatCard> {
    let mut all = Vec::new();
    all.extend(downfall_cards());
    all.extend(public_proof_cards());
    all.extend(identity_cards());
    all.extend(suspense_cards());
    all.extend(romance_cards());
    all.extend(system_cards());
    all.extend(backstage_cards());
    all
}

